func TestChainCollectorRespectsCap(t *testing.T) {
	bulk := stubCollector{out: make([]Rule, MaxRules+10)}
	for i := range bulk.out {
		bulk.out[i] = Rule{Engine: EngineIPTables, RuleHash: itoa(i)}
	}
	chain := &chainCollector{collectors: []Collector{bulk}}
	got, err := chain.Collect(context.Background())
	if err != nil {
		t.Fatalf("Collect: %v", err)
	}
	if len(got) != MaxRules {
		t.Fatalf("want %d (cap), got %d", MaxRules, len(got))
	}
}

