func TestIPTablesCollectorEndToEnd(t *testing.T) {
	c := &iptablesCollector{
		binary:   "iptables-save",
		lookPath: func(string) (string, error) { return "/sbin/iptables-save", nil },
		run: func(_ context.Context, _ string, _ ...string) ([]byte, error) {
			return []byte(`*filter
:INPUT ACCEPT [0:0]
-A INPUT -p tcp --dport 22 -j ACCEPT
-A INPUT -j DROP
COMMIT
`), nil
		},
	}
	got, err := c.Collect(context.Background())
	if err != nil {
		t.Fatalf("Collect: %v", err)
	}
	if len(got) != 2 {
		t.Fatalf("want 2 rules, got %d", len(got))
	}
	for _, r := range got {
		if r.RuleHash == "" {
			t.Fatalf("rule hash not stamped: %+v", r)
		}
		if r.Engine != EngineIPTables {
			t.Fatalf("engine not stamped: %q", r.Engine)
		}
	}
}

