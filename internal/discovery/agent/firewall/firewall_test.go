func TestPinnedEnumStrings(t *testing.T) {
	pairs := []struct{ got, want string }{
		{string(EngineIPTables), "iptables"},
		{string(EngineNFTables), "nftables"},
		{string(EnginePF), "pf"},
		{string(EngineWindowsFW), "windows-firewall"},
		{string(EngineUFW), "ufw"},
		{string(EngineFirewalld), "firewalld"},
		{string(EngineUnknown), "unknown"},
		{string(DirectionIn), "in"},
		{string(DirectionOut), "out"},
		{string(DirectionForward), "forward"},
		{string(DirectionUnknown), "unknown"},
		{string(ActionAccept), "accept"},
		{string(ActionDrop), "drop"},
		{string(ActionReject), "reject"},
		{string(ActionLog), "log"},
		{string(ActionJump), "jump"},
		{string(ActionReturn), "return"},
		{string(ActionUnknown), "unknown"},
	}
	for _, p := range pairs {
		if p.got != p.want {
			t.Fatalf("enum drift: got %q want %q (breaks SQLite CHECK)",
				p.got, p.want)
		}
	}
}

