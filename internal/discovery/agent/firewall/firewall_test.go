func TestIsPermissive(t *testing.T) {
	cases := []struct {
		r    Rule
		want bool
	}{
		{Rule{Action: ActionAccept, SrcCIDR: ""}, true},          // bare ACCEPT = any source
		{Rule{Action: ActionAccept, SrcCIDR: "0.0.0.0/0"}, true}, // explicit any v4
		{Rule{Action: ActionAccept, SrcCIDR: "::/0"}, true},      // any v6
		{Rule{Action: ActionAccept, SrcCIDR: "any"}, true},       // pf shorthand
		{Rule{Action: ActionAccept, SrcCIDR: "10.0.0.0/8"}, false},
		{Rule{Action: ActionDrop, SrcCIDR: "0.0.0.0/0"}, false}, // DROP from any = restrictive
		{Rule{Action: ActionReject, SrcCIDR: ""}, false},
	}
	for i, tc := range cases {
		if got := IsPermissive(tc.r); got != tc.want {
			t.Fatalf("case %d: IsPermissive(%+v) = %v, want %v",
				i, tc.r, got, tc.want)
		}
	}
}

func TestHashRuleIsDeterministicAndIgnoresPriority(t *testing.T) {
	a := Rule{
		Engine: EngineIPTables, Chain: "INPUT", Direction: DirectionIn,
		Action: ActionAccept, Proto: "tcp", DstPort: "22", Priority: 1,
	}
	b := a
	b.Priority = 99 // priority should not affect hash

	ha, hb := HashRule(a), HashRule(b)
	if ha != hb {
		t.Fatalf("HashRule must ignore Priority: %q != %q", ha, hb)
	}
	if len(ha) != 64 {
		t.Fatalf("expected sha256 hex (64 chars), got %d", len(ha))
	}

	c := a
	c.DstPort = "80"
	hc := HashRule(c)
	if hc == ha {
		t.Fatalf("HashRule must change when DstPort changes")
	}
}

func TestParseIPTablesSaveRealisticFixture(t *testing.T) {
	raw := `# Generated by iptables-save v1.8.7 on Mon Jun 23 12:00:00 2026
*filter
:INPUT ACCEPT [0:0]
:FORWARD DROP [0:0]
:OUTPUT ACCEPT [0:0]
-A INPUT -i lo -j ACCEPT
-A INPUT -p tcp -m tcp --dport 22 -j ACCEPT
-A INPUT -p tcp -m tcp --dport 5432 -s 0.0.0.0/0 -j ACCEPT
-A INPUT -p icmp -j ACCEPT
-A INPUT -m state --state RELATED,ESTABLISHED -j ACCEPT
-A INPUT -j DROP
-A FORWARD -i eth0 -o eth1 -j ACCEPT
COMMIT
# Completed on Mon Jun 23 12:00:00 2026
`
	rules := parseIPTablesSave(raw)
	if len(rules) != 7 {
		t.Fatalf("want 7 rules, got %d", len(rules))
	}
	// Spot-check the permissive PG rule (rule index 2).
	pg := rules[2]
	if pg.Chain != "INPUT" {
		t.Fatalf("pg chain=%q", pg.Chain)
	}
	if pg.Direction != DirectionIn {
		t.Fatalf("pg direction=%q", pg.Direction)
	}
	if pg.Action != ActionAccept {
		t.Fatalf("pg action=%q", pg.Action)
	}
	if pg.Proto != "tcp" || pg.DstPort != "5432" {
		t.Fatalf("pg proto/port lost: %q/%q", pg.Proto, pg.DstPort)
	}
	if pg.SrcCIDR != "0.0.0.0/0" {
		t.Fatalf("pg src lost: %q", pg.SrcCIDR)
	}
	if !IsPermissive(pg) {
		t.Fatalf("pg should be permissive: %+v", pg)
	}

	// Forward rule has explicit interfaces.
	fwd := rules[6]
	if fwd.Direction != DirectionForward {
		t.Fatalf("forward direction=%q", fwd.Direction)
	}
	if fwd.IfaceIn != "eth0" || fwd.IfaceOut != "eth1" {
		t.Fatalf("forward interfaces lost: in=%q out=%q",
			fwd.IfaceIn, fwd.IfaceOut)
	}

	// Stateful match goes to Extras.
	stateful := rules[4]
	if stateful.Extras == "" {
		t.Fatalf("stateful match should populate Extras")
	}
}

