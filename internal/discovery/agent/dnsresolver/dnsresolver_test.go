// -- ParseSystemdResolvedConf -------------------------------------------

func TestParseSystemdResolvedTypical(t *testing.T) {
	body := []byte(`# This file is /etc/systemd/resolved.conf
[Resolve]
DNS=1.1.1.1 1.0.0.1
FallbackDNS=8.8.8.8
Domains=~corp.local lab.internal
DNSSEC=yes
DNSOverTLS=opportunistic
`)
	got := ParseSystemdResolvedConf(body, "/etc/systemd/resolved.conf")
	if len(got) != 3 {
		t.Fatalf("len=%d, want 3: %+v", len(got), got)
	}
	for _, r := range got {
		if r.Source != SourceSystemdResolved {
			t.Fatalf("source=%q", r.Source)
		}
		if !r.IsDNSSEC {
			t.Fatal("DNSSEC=yes must propagate")
		}
		if r.Protocol != ProtocolDoT {
			t.Fatalf("DNSOverTLS=opportunistic → protocol must be DoT; got %q", r.Protocol)
		}
		if r.Port != 853 {
			t.Fatalf("DoT port must be 853; got %d", r.Port)
		}
		if len(r.SearchDomains) != 2 {
			t.Fatalf("domains=%v", r.SearchDomains)
		}
		if !r.IsDoHOrDoT {
			t.Fatal("DoT must flag is_doh_or_dot")
		}
	}
	// Fallback row gets Priority=100.
	var fallback Resolver
	for _, r := range got {
		if r.Server == "8.8.8.8" {
			fallback = r
		}
	}
	if fallback.Priority != 100 {
		t.Fatalf("fallback priority=%d, want 100", fallback.Priority)
	}
}

