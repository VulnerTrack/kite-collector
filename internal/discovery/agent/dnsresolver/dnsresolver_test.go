func TestIsPublicResolverIP(t *testing.T) {
	hit := []string{
		"8.8.8.8", "1.1.1.1", "9.9.9.9", "208.67.222.222",
		"2606:4700:4700::1111", "2606:4700:4700::1001",
	}
	for _, s := range hit {
		if !IsPublicResolverIP(s) {
			t.Fatalf("%q must flag public", s)
		}
	}
	miss := []string{"10.0.0.1", "192.168.1.1", "fc00::1", "", "not-an-ip"}
	for _, s := range miss {
		if IsPublicResolverIP(s) {
			t.Fatalf("%q must NOT flag public", s)
		}
	}
}

