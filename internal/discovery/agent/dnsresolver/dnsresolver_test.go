func TestFileCollectorWalksAllSources(t *testing.T) {
	tmp := t.TempDir()
	resolvConf := filepath.Join(tmp, "resolv.conf")
	resolved := filepath.Join(tmp, "resolved.conf")
	resolvedDir := filepath.Join(tmp, "resolved.conf.d")
	nmDir := filepath.Join(tmp, "system-connections")
	dnsmasq := filepath.Join(tmp, "dnsmasq.conf")
	dnsmasqDir := filepath.Join(tmp, "dnsmasq.d")

	must(t, os.MkdirAll(resolvedDir, 0o755))
	must(t, os.MkdirAll(nmDir, 0o755))
	must(t, os.MkdirAll(dnsmasqDir, 0o755))

	mustWrite(t, resolvConf, "nameserver 10.0.0.53\n")
	mustWrite(t, resolved, "[Resolve]\nDNS=1.1.1.1\n")
	mustWrite(t, filepath.Join(resolvedDir, "10-corp.conf"),
		"[Resolve]\nDNS=10.0.0.54\n")
	mustWrite(t, filepath.Join(nmDir, "eth0.nmconnection"),
		"[connection]\ninterface-name=eth0\n[ipv4]\ndns=10.0.0.55;\n")
	mustWrite(t, dnsmasq, "server=10.0.0.56\n")
	mustWrite(t, filepath.Join(dnsmasqDir, "00-extra.conf"),
		"server=/corp.local/10.0.0.57\n")

	c := &fileCollector{
		resolvConf:       resolvConf,
		resolvedConf:     resolved,
		resolvedConfDir:  resolvedDir,
		nmConnectionsDir: nmDir,
		dnsmasqConf:      dnsmasq,
		dnsmasqConfDir:   dnsmasqDir,
		readFile:         os.ReadFile,
		readDir:          os.ReadDir,
	}
	got, err := c.Collect(context.Background())
	if err != nil {
		t.Fatalf("Collect: %v", err)
	}
	// 1 resolv + 1 systemd main + 1 systemd dropin + 1 NM + 1 dnsmasq + 1 dnsmasq dropin = 6.
	if len(got) != 6 {
		t.Fatalf("want 6 rows, got %d: %+v", len(got), got)
	}

	// Confirm we got the public-resolver flag on 1.1.1.1 from
	// systemd-resolved.conf.
	var publicFound bool
	for _, r := range got {
		if r.IsPublicResolver && r.Server == "1.1.1.1" {
			publicFound = true
		}
	}
	if !publicFound {
		t.Fatal("1.1.1.1 must flag as public resolver")
	}

	// Confirm the per-domain dnsmasq dropin landed correctly.
	var perDomain bool
	for _, r := range got {
		if r.RoutedDomain == "corp.local" {
			perDomain = true
		}
	}
	if !perDomain {
		t.Fatal("per-domain dnsmasq route lost across collector")
	}
}

