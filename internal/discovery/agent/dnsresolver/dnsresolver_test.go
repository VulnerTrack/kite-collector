func TestAnnotateSecurity(t *testing.T) {
	r := Resolver{Server: "8.8.8.8", Protocol: ProtocolUDP}
	AnnotateSecurity(&r)
	if !r.IsPublicResolver || r.IsLoopback || r.IsDoHOrDoT {
		t.Fatalf("8.8.8.8/udp wrong flags: %+v", r)
	}

	r = Resolver{Server: "127.0.0.53", Protocol: ProtocolUDP}
	AnnotateSecurity(&r)
	if r.IsPublicResolver || !r.IsLoopback || r.IsDoHOrDoT {
		t.Fatalf("127.0.0.53/udp wrong flags: %+v", r)
	}

	r = Resolver{Server: "1.1.1.1", Protocol: ProtocolDoH}
	AnnotateSecurity(&r)
	if !r.IsPublicResolver || r.IsLoopback || !r.IsDoHOrDoT {
		t.Fatalf("1.1.1.1/doh wrong flags: %+v", r)
	}
}

// -- ParseResolvConf -----------------------------------------------------

func TestParseResolvConfTypical(t *testing.T) {
	body := []byte(`# Generated by resolvconf
search corp.local lab.internal
nameserver 10.0.0.53
nameserver 10.0.0.54
options edns0 trust-ad
`)
	got := ParseResolvConf(body, "/etc/resolv.conf")
	if len(got) != 2 {
		t.Fatalf("len=%d, want 2", len(got))
	}
	for _, r := range got {
		if r.Source != SourceResolvConf {
			t.Fatalf("source=%q", r.Source)
		}
		if r.Port != 53 || r.Protocol != ProtocolUDP {
			t.Fatalf("port/proto=%d/%q", r.Port, r.Protocol)
		}
		if !r.IsDNSSEC {
			t.Fatal("edns0+trust-ad must flag dnssec")
		}
		if len(r.SearchDomains) != 2 {
			t.Fatalf("search=%v", r.SearchDomains)
		}
		if r.IsPublicResolver {
			t.Fatalf("10.0.0.53 must NOT flag public: %+v", r)
		}
		if r.FileHash == "" {
			t.Fatal("file_hash missing")
		}
	}
}

