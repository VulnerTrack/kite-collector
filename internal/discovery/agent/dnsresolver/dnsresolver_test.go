func TestFileCollectorRespectsLexicalOrder(t *testing.T) {
	tmp := t.TempDir()
	resolvedDir := filepath.Join(tmp, "resolved.conf.d")
	must(t, os.MkdirAll(resolvedDir, 0o755))
	mustWrite(t, filepath.Join(resolvedDir, "20-late.conf"),
		"[Resolve]\nDNS=10.0.0.20\n")
	mustWrite(t, filepath.Join(resolvedDir, "10-early.conf"),
		"[Resolve]\nDNS=10.0.0.10\n")

	c := &fileCollector{
		resolvConf:       "/nope",
		resolvedConf:     "/nope",
		resolvedConfDir:  resolvedDir,
		nmConnectionsDir: "/nope",
		dnsmasqConf:      "/nope",
		dnsmasqConfDir:   "/nope",
		readFile:         os.ReadFile,
		readDir:          os.ReadDir,
	}
	got, err := c.Collect(context.Background())
	if err != nil {
		t.Fatal(err)
	}
	if len(got) != 2 {
		t.Fatalf("len=%d", len(got))
	}
	// The sort step normalises ordering to (source, server, port) so we
	// can't assert on read order — but both servers must appear.
	servers := []string{got[0].Server, got[1].Server}
	if !contains(servers, "10.0.0.10") || !contains(servers, "10.0.0.20") {
		t.Fatalf("expected both drop-ins read, got %v", servers)
	}
}

